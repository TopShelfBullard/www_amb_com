  <h2>HTML/CSS</h2>
  <p>
    HTML and CSS&mdash;where some developers choose to organize their content and style and others choose to design 
    something more akin to the tangled nest of power cords behind an entertainment system. Few have found a method to
    strengthen one's patience that can hope to rival the experience of witnessing four browsers render your flawless code
    like infants who stumbled upon a box of markers. 
  </p>
  <p>
    Luckily for me, as a father of five, I have more than enough patience to deal with four unruly browsers, and I always enjoy
    a good challenge. Actually, I have had a blast learning HTML and CSS. I designed the site that you see here as an excuse to
    research Bootstrap 3 and LESS. During the development of this site I came to a much greater understanding of mobile first
    and responsive design, I dug into the Bootstrap 3 code and learned the techniques of creating a fluid grid so that I could alter
    it to better fit my project, and I gained the practical experience of compiling code with CodeKit and deploying on Heroku.
  </p>

  <h2>JavaScript</h2>
  <p>
    Ahh, JavaScript, the language that insists an array of one, two, three, and four is not equal to an array of one, two, three
    and four... although ten quadrillion is, in fact, equal to ten quadrillion and one. Isn't it amazing that in merely ten days,
    Brendan Eich of Netscape won the race against Microsoft to develop a language with the power to rob a computer of its
    ability to understand equality? I hear the kids are using JavaScript all over the place these days. The statisticians are saying
    that there is a new JavaScript library released like every 7.3 seconds and I think NASA just announced a plan rewrite all of
    their software for the space shuttles in jQuery.
  </p>
  <p>
    I enjoy making fun of JavaScript, but I must admit that it was a programming fundamentals course with a spectacular professor
    and none other that plain old JavaScript that initially hooked me on programming. At the time I did little else other than work
    on JavaScript and by the end of the eight week course I was working comfortably with the DOM, experimenting with OOP, and
    beginning to branch out into jQuery. I still enjoy JavaScript very much, and I recently wrote a little web app called "The Revisionist"
    that you can check out by clicking the 'app' link above.
  </p>


  <h2>Ruby/Rails</h2>
  <p>
    As a young developer I was told stories of a magical language where all that that a developer would have to do is consider
    opening a text editor and they will have inadvertently created fifteen objects and replaced three hundred lines of code
    with a handful of words separated by dots. Initially I dismissed this as merely fanciful bedtime stories that our professors used
    to calm us after epic debugging sessions, but then I met Ruby.
  </p>
  <p>
    I have worked on Ruby and Ruby on rails quite a bit this summer. Ruby is the first language that forced me to confront objects
    head on, and I am very thankful that I met the challenge because I now have a firm understanding of the concepts of object
    oriented programming. Actually, partially due to good instincts and partially due to great instruction, I wrote much of my 
    JavaScript code with the principles of encapsilation and abstraction in mind, so the transition to OOP seem quite natural.
  </p>
  <p>
    I soon moved on to Ruby on Rails, where the learning curve for me was steep indeed. Rails was my first exposure the the MVC
    pattern, as well as the first time I left the client side to see how good the view was from the server. Configuration tied me up for
    awhile, and I am ashamed to admit that I spoke very uncharitable words to the poor Ruby Gems in my GEMFILE. All of that
    is in the rear view mirror now, though, and a summer of Rails development has earned me a great deal of useful bonus knowledge,
    such as working with MySQL and git. Ruby on Rails has been a great experience so far, and I am very excited about the release of
    my first Rails web app, "Nerdwords," which you can check out by clicking the 'app' link above.
  </p>



